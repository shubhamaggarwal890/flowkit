package org.example.flowkit.jsonobject;

public class RoleRequest {
    private String id;
    private String name;

    public RoleRequest() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
